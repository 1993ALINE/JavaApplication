package arithmaticoperator;

/**
 *
 * @author ALINE
 */
public class ArithmaticOperator {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int i = 40;
        int j = 50;
        
        System.out.println(i+j);
        System.out.println(i-j);
        System.out.println(i*j);
        System.out.println(i/j); 
        System.out.println(i%j);
        
    }
    
}
