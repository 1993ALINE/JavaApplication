package switchcase;

/**
 *
 * @author ALINE
 */
public class SwitchCase {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int i = 4;
        switch(i){
            case 0:
                System.out.println("You are wise");
            case 1:
                System.out.println("You are not so wise");
            case 2:
                System.out.println("You are fool");
            case 3:
                System.out.println("You are very fool");
                     
            
        }




    }
    
}
